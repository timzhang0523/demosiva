$('.haveChild span').each(function () {
    var child_class = $(this).attr('data-class');
    if (child_class == 'nav-menu-contract') {
        $(this).removeClass('nav-menu-contract');
        $(this).addClass('nav-menu-expand');
        $(this).attr('data-class', 'nav-menu-expand');
        $(this).siblings('.menu').show();
    }

    if (child_class == 'nav-menu-expand') {
        $(this).removeClass('nav-menu-expand');
        $(this).addClass('nav-menu-contract');
        $(this).attr('data-class', 'nav-menu-contract');
        $(this).siblings('.menu').hide();
    }

})


